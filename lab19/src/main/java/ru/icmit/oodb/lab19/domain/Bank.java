package ru.icmit.oodb.lab19.domain;

import javax.persistence.*;

@Entity
public class Bank {

    @Id
    private Long id;

    private String name;


    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
